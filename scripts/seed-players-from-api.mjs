// One-off data tool — NOT part of the running app.
// Pulls every NBA player from the free BALLDONTLIE API, filters to the
// 2000-and-later era, and writes a SQL file you review and run yourself
// in Supabase's SQL editor. This script never touches your database
// directly — it only reads from BALLDONTLIE and writes a local file.
//
// Usage:
//   1. Sign up for a free API key at https://app.balldontlie.io
//   2. BALLDONTLIE_API_KEY=your_key_here node scripts/seed-players-from-api.mjs
//   3. Review supabase/seed_players.sql, then paste it into Supabase's SQL editor
//
// Heads up: the free tier allows only 5 requests/minute, so this script
// deliberately runs slowly (~13s between pages) to avoid getting rate
// limited. Expect it to take several minutes for the full player list —
// let it run in the background, it will finish on its own.
//
// Why "draft_year >= 2000" is the filter: since it's mid-2026 as this is
// written, virtually nobody drafted before 2000 is still active or recently
// relevant — NBA careers essentially never span 26+ years. This filter is
// a close approximation of "everyone who mattered in the league since 2000"
// without needing to aggregate decades of season-by-season stats.

import { writeFile } from "node:fs/promises";

const API_KEY = process.env.BALLDONTLIE_API_KEY;
if (!API_KEY) {
  console.error("Missing BALLDONTLIE_API_KEY. Set it and try again, e.g.:");
  console.error("  BALLDONTLIE_API_KEY=your_key_here node scripts/seed-players-from-api.mjs");
  process.exit(1);
}

const BASE_URL = "https://api.balldontlie.io/nba/v1/players";
const MIN_DRAFT_YEAR = 2000;

// BALLDONTLIE's free tier allows only 5 requests per minute. 13 seconds
// between requests keeps us safely under that (60s / 5 = 12s minimum,
// with a small buffer). This makes the full run slow (potentially several
// minutes for a few thousand players across ~30-50 pages) but reliable.
const DELAY_BETWEEN_REQUESTS_MS = 13_000;

function escapeSqlString(value) {
  return value.replace(/'/g, "''");
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

async function fetchWithRetry(url) {
  while (true) {
    const res = await fetch(url, { headers: { Authorization: API_KEY } });

    if (res.status === 429) {
      // Respect the server's Retry-After header if it sends one;
      // otherwise fall back to waiting a full minute before trying again.
      const retryAfterHeader = res.headers.get("retry-after");
      const retryAfterMs = retryAfterHeader ? Number(retryAfterHeader) * 1000 : 60_000;
      console.log(`Rate limited — waiting ${Math.round(retryAfterMs / 1000)}s before retrying…`);
      await sleep(retryAfterMs);
      continue;
    }

    if (!res.ok) {
      throw new Error(`BALLDONTLIE request failed: ${res.status} ${res.statusText}`);
    }

    return res.json();
  }
}

async function fetchAllPlayers() {
  const players = [];
  let cursor = null;
  let page = 0;

  while (true) {
    const url = new URL(BASE_URL);
    url.searchParams.set("per_page", "100");
    if (cursor) url.searchParams.set("cursor", String(cursor));

    const json = await fetchWithRetry(url);
    players.push(...json.data);
    page += 1;
    console.log(`Fetched page ${page} (${players.length} players so far)…`);

    cursor = json.meta?.next_cursor ?? null;
    if (!cursor) break;

    await sleep(DELAY_BETWEEN_REQUESTS_MS);
  }

  return players;
}

function isRelevant(player) {
  // Must currently be attached to a team — retired/unassigned players
  // aren't useful for puzzles about current rosters.
  if (!player.team) return false;

  // Drafted 2000 or later, OR undrafted (draft_year null) but currently
  // rostered — covers international/undrafted players who are clearly
  // still active regardless of when they technically entered the league.
  if (player.draft_year != null) return player.draft_year >= MIN_DRAFT_YEAR;
  return true;
}

async function main() {
  console.log("Fetching players from BALLDONTLIE…");
  const allPlayers = await fetchAllPlayers();

  const relevant = allPlayers.filter(isRelevant);
  console.log(`\n${allPlayers.length} total players fetched, ${relevant.length} kept after filtering.`);

  const rows = relevant.map((p) => {
    const fullName = escapeSqlString(`${p.first_name} ${p.last_name}`.trim());
    const abbreviation = escapeSqlString(p.team.abbreviation);
    return `  ('${fullName}', (select id from teams where abbreviation = '${abbreviation}'), null)`;
  });

  const sql = `-- Seed: NBA players drafted ${MIN_DRAFT_YEAR} or later, currently rostered
-- Generated by scripts/seed-players-from-api.mjs from the BALLDONTLIE API.
-- Run this in the Supabase SQL editor AFTER schema.sql and seed_teams.sql.
--
-- headshot_url is left null for all of these -- BALLDONTLIE doesn't provide
-- player images. Add a headshot_url later per-player through /admin/players
-- if you want a photo to show for a specific puzzle.
--
-- If a player's team abbreviation doesn't match any row in your teams
-- table, the subquery returns null and that player is inserted with no
-- team rather than failing the whole batch -- check for any null team_id
-- rows afterward if that matters to you.

insert into players (name, team_id, headshot_url) values
${rows.join(",\n")};
`;

  const outputPath = new URL("../supabase/seed_players.sql", import.meta.url);
  await writeFile(outputPath, sql, "utf-8");
  console.log(`\nWrote ${relevant.length} players to supabase/seed_players.sql`);
}

main().catch((err) => {
  console.error("Failed:", err.message);
  process.exit(1);
});
